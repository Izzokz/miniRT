#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t			index;
	unsigned char	*str1;
	unsigned char	*str2;

	index = 0;
	str1 = (unsigned char *)s1;
	str2 = (unsigned char *)s2;
	while (index < n && str1[index] == str2[index])
		index++;
	if (index >= n)
		return (0);
	return (str1[index] - str2[index]);
}
