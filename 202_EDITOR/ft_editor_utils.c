#include "miniRT.h"

char	not_numeric_free(char *s)
{
	int	i;

	i = -1;
	while (*(s + ++i))
	{
		if (*(s + i) == '\n')
			break ;
		if (*(s + i) < '0' || *(s + i) > '9')
		{
			free(s);
			return (1);
		}
	}
	return (0);
}
